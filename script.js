// Scroll Animations
// ============================================

const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -100px 0px'
};

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('visible');
        }
    });
}, observerOptions);

// Observe all sections
document.querySelectorAll('.section').forEach(section => {
    section.classList.add('fade-in');
    observer.observe(section);
});

// Observe timeline items
document.querySelectorAll('.timeline-item').forEach((item, index) => {
    item.style.opacity = '0';
    item.style.transform = 'translateX(-30px)';
    item.style.transition = `opacity 0.6s ease-out ${index * 0.1}s, transform 0.6s ease-out ${index * 0.1}s`;
    
    const itemObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateX(0)';
            }
        });
    }, observerOptions);
    
    itemObserver.observe(item);
});

// Observe project cards
document.querySelectorAll('.project-card').forEach((card, index) => {
    card.style.opacity = '0';
    card.style.transform = 'translateY(30px)';
    card.style.transition = `opacity 0.6s ease-out ${index * 0.1}s, transform 0.6s ease-out ${index * 0.1}s`;
    
    const cardObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.style.opacity = '1';
                entry.target.style.transform = 'translateY(0)';
            }
        });
    }, observerOptions);
    
    cardObserver.observe(card);
});

// ============================================
// Title Animation (Typewriter Effect)
// ============================================

const titleItems = document.querySelectorAll('.title-item');
let currentTitleIndex = 0;

function animateTitles() {
    if (titleItems.length === 0) return;
    
    // Hide all titles initially
    titleItems.forEach((item, index) => {
        if (index !== currentTitleIndex) {
            item.style.opacity = '0';
            item.style.transform = 'translateY(20px)';
        }
    });
    
    // Show current title
    const currentTitle = titleItems[currentTitleIndex];
    currentTitle.style.opacity = '1';
    currentTitle.style.transform = 'translateY(0)';
    
    // Move to next title
    setTimeout(() => {
        currentTitle.style.opacity = '0';
        currentTitle.style.transform = 'translateY(-20px)';
        
        currentTitleIndex = (currentTitleIndex + 1) % titleItems.length;
        
        setTimeout(() => {
            animateTitles();
        }, 300);
    }, 3000);
}

// Start title animation after a delay
setTimeout(() => {
    animateTitles();
}, 1000);

